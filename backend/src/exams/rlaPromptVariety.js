const RECENT_WINDOW = 8;

const cursorState = {
  readingInformational: 0,
  readingLiterary: 0,
  essay: 0,
  language: 0,
};

const recentState = {
  readingInformational: [],
  readingLiterary: [],
  essay: [],
  language: [],
};

function orderedFromCursor(pool, cursor) {
  if (!Array.isArray(pool) || pool.length === 0) return [];
  const start = ((cursor % pool.length) + pool.length) % pool.length;
  return pool.slice(start).concat(pool.slice(0, start));
}

function rememberSelection(key, selectedIds) {
  const ring = recentState[key];
  if (!Array.isArray(ring)) return;
  ring.push(...selectedIds);
  while (ring.length > RECENT_WINDOW) ring.shift();
}

function selectDistinct(pool, count, key) {
  if (!Array.isArray(pool) || pool.length === 0 || count <= 0) return [];

  const ordered = orderedFromCursor(pool, cursorState[key] || 0);
  const recent = new Set(recentState[key] || []);
  const fresh = ordered.filter((entry) => !recent.has(entry.id));
  const stale = ordered.filter((entry) => recent.has(entry.id));
  const selected = fresh.concat(stale).slice(0, Math.min(count, pool.length));

  cursorState[key] = ((cursorState[key] || 0) + count) % pool.length;
  rememberSelection(
    key,
    selected.map((entry) => entry.id)
  );

  return selected;
}

function nextRlaReadingBlueprintSet() {
  const informational = selectDistinct(
    RLA_READING_INFORMATIONAL_BLUEPRINTS,
    3,
    'readingInformational'
  );
  const literary = selectDistinct(
    RLA_READING_LITERARY_BLUEPRINTS,
    1,
    'readingLiterary'
  )[0];

  return {
    informational,
    literary,
  };
}

function nextRlaEssayBlueprint() {
  return selectDistinct(RLA_ESSAY_POLICY_BLUEPRINTS, 1, 'essay')[0] || null;
}

function nextRlaLanguageBlueprintSet() {
  return selectDistinct(RLA_LANGUAGE_DOCUMENT_BLUEPRINTS, 7, 'language');
}

function resetRlaPromptVarietyState() {
  cursorState.readingInformational = 0;
  cursorState.readingLiterary = 0;
  cursorState.essay = 0;
  cursorState.language = 0;
  recentState.readingInformational.length = 0;
  recentState.readingLiterary.length = 0;
  recentState.essay.length = 0;
  recentState.language.length = 0;
}

